#include "input.h"
#include "manager.h"
#include "debugproc.h"

//================================================================
//静的メンバ変数
//================================================================
LPDIRECTINPUT8 CInput::m_pInput = NULL;

//================================================================
//コンストラクタ
//================================================================
CInput::CInput()
{
	m_pDevice = NULL;
}

//================================================================
//デストラクタ
//================================================================
CInput::~CInput()
{

}

//================================================================
//初期化処理
//================================================================
HRESULT CInput::Init(HINSTANCE hInstance, HWND hWnd)
{
	if (m_pInput == NULL)
	{
		//directオブジェクトの生成
		if (FAILED(DirectInput8Create(hInstance, DIRECTINPUT_VERSION, IID_IDirectInput8, (void**)&m_pInput, NULL)))
		{
			return E_FAIL;
		}
	}

	return S_OK;
}

//================================================================
//キーボードの終了処理
//================================================================
void CInput::Uninit(void)
{
	//入力デバイス（キーボード）の破棄
	if (m_pDevice != NULL)
	{
		m_pDevice->Unacquire();
		m_pDevice->Release();
		m_pDevice = NULL;
	}

	//DirectInputオブジェクトの破棄
	if (m_pInput != NULL)
	{
		m_pInput->Release();
		m_pInput = NULL;
	}
}

